let Animals = ["Dog", "Cat", "Parrots"];

for (let a of Animals) {
    if (a == "Dog"){
        console.log(`A ${a} would make a Loyal pet.`);
    }else if (a == "Cat"){
        console.log(`A ${a} would make a Caring pet.`);
    }else if (a == "Parrots"){
        console.log(`A ${a} would make a Enthusistic pet.`);
    }
}

console.log("Any of these animals would make a great pet!");